const { Client, Databases } = require('node-appwrite');
require('dotenv').config({ path: '.env.local' });

const client = new Client()
  .setEndpoint(process.env.NEXT_PUBLIC_APPWRITE_ENDPOINT)
  .setProject(process.env.NEXT_PUBLIC_APPWRITE_PROJECT_ID)
  .setKey(process.env.APPWRITE_API_KEY);

const databases = new Databases(client);
const DATABASE_ID = process.env.NEXT_PUBLIC_APPWRITE_DATABASE_ID;

async function fixCommentsAttributes() {
  console.log('üîß –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –∞—Ç—Ä–∏–±—É—Ç–æ–≤ –≤ –∫–æ–ª–ª–µ–∫—Ü–∏–∏ comments...\n');
  
  const missingAttributes = [
    { key: 'job_id', type: 'string', size: 255, required: true },
    { key: 'user_id', type: 'string', size: 255, required: true },
    { key: 'user_name', type: 'string', size: 255, required: true },
    { key: 'user_avatar', type: 'string', size: 500, required: false },
    { key: 'content', type: 'string', size: 2000, required: true },
    { key: 'type', type: 'string', size: 50, required: false, default: 'comment' },
    { key: 'parent_id', type: 'string', size: 255, required: false },
    { key: 'likes', type: 'integer', required: false, min: 0, default: 0 },
    { key: 'dislikes', type: 'integer', required: false, min: 0, default: 0 },
    { key: 'created_at', type: 'datetime', required: true },
    { key: 'updated_at', type: 'datetime', required: false },
  ];

  try {
    // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ –∫–æ–ª–ª–µ–∫—Ü–∏–∏ comments
    const commentsCollection = await databases.listDocuments(DATABASE_ID, 'comments');
    console.log(`‚úÖ –ö–æ–ª–ª–µ–∫—Ü–∏—è comments –Ω–∞–π–¥–µ–Ω–∞ (${commentsCollection.documents.length} –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤)`);
    
    // –î–æ–±–∞–≤–ª—è–µ–º –Ω–µ–¥–æ—Å—Ç–∞—é—â–∏–µ –∞—Ç—Ä–∏–±—É—Ç—ã
    for (const attr of missingAttributes) {
      try {
        await new Promise(resolve => setTimeout(resolve, 500)); // –ó–∞–¥–µ—Ä–∂–∫–∞ –º–µ–∂–¥—É –∑–∞–ø—Ä–æ—Å–∞–º–∏
        
        if (attr.type === 'string') {
          await databases.createStringAttribute(
            DATABASE_ID,
            'comments',
            attr.key,
            attr.size,
            attr.required
          );
        } else if (attr.type === 'integer') {
          await databases.createIntegerAttribute(
            DATABASE_ID,
            'comments',
            attr.key,
            attr.required,
            attr.min
          );
        } else if (attr.type === 'datetime') {
          await databases.createDatetimeAttribute(
            DATABASE_ID,
            'comments',
            attr.key,
            attr.required
          );
        }
        
        console.log(`  ‚ûï –î–æ–±–∞–≤–ª–µ–Ω –∞—Ç—Ä–∏–±—É—Ç: ${attr.key} (${attr.type})`);
      } catch (attrError) {
        if (attrError.message.includes('already exists')) {
          console.log(`  ‚ö†Ô∏è –ê—Ç—Ä–∏–±—É—Ç ${attr.key} —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç`);
        } else if (attrError.message.includes('maximum number')) {
          console.log(`  ‚ö†Ô∏è –î–æ—Å—Ç–∏–≥–Ω—É—Ç –ª–∏–º–∏—Ç –∞—Ç—Ä–∏–±—É—Ç–æ–≤ –¥–ª—è ${attr.key}`);
        } else {
          console.log(`  ‚ùå –û—à–∏–±–∫–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è ${attr.key}: ${attrError.message}`);
        }
      }
    }
    
    console.log('\n‚úÖ –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∞—Ç—Ä–∏–±—É—Ç–æ–≤ comments –∑–∞–≤–µ—Ä—à–µ–Ω–æ!');
    
  } catch (error) {
    console.error('‚ùå –û—à–∏–±–∫–∞ —Ä–∞–±–æ—Ç—ã —Å –∫–æ–ª–ª–µ–∫—Ü–∏–µ–π comments:', error.message);
  }
}

fixCommentsAttributes().catch(console.error);
